package temperature;

public class Temperature {

    public static void main(String[] args) { 
       int a=289;
       int b=400;
       int c=-36;
       int d=-180;
       
       System.out.println("289 Celsius in Fahrenheit is: "+ ((a*9/5)+32));
       System.out.println("400 Celsius in Fahrenheit is: "+ ((b*9/5)+32));
       System.out.println("-36 Celsius in Fahrenheit is: "+((c*9/5)+32));
       System.out.println("-180 Celsius in Fahrenheit is: "+((d*9/5)+32));
    }
}
